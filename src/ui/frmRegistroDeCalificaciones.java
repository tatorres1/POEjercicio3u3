    private boolean validar (){
        float nota = (float )this.spnValorCalificacion.getValue();
        if(nota== 0.f){
            verMensaje("Calificacion no valida",JOptionPane.INFORMATION_MESSAGE);
        }
        return true;
    }
    
    
    public void consultarMatricula() throws IOException {
        String numero = this.txtNumeroMatricula.getText();
        matriculabll.consultar(numero);
        this.txtDatosMatricula.setText(matriculabll.imprimir());
    }

